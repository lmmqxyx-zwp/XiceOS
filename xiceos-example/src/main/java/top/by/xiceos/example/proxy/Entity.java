package top.by.xiceos.example.proxy;

/**
 * <p>Title: Entity</p>
 * <p>Description: 实体类</p>
 *
 * @author zwp
 * @date 2018/10/19 17:00
 */
public class Entity {

    /**
     * 名称
     */
    private String name;

    /**
     * 版本
     */
    private int version;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getVersion() {
        return version;
    }

    public void setVersion(int version) {
        this.version = version;
    }
}
